package main
import "fmt"
func main() {
    var a int=30  //declariing two integers
    var b int=10
    
    fmt.Println("Go Arithmetic Operators")
    
    
    fmt.Println(a+b) // addition
    fmt.Println(a-b) // subtraction
    fmt.Println(a*b) // multiplication
    fmt.Println(a/b) // division
    fmt.Println(a%b) //modulo
   
}
